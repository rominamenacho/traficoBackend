package com.nuebus.dto;

import java.util.Date;
import java.util.HashSet;
import java.util.Set;

/**
 *
 * @author Usuario
 */
public class DiagramacionDTO {

    private Long id;
    private String empCodigo;
    private java.util.Date fechaDesde;
    private java.util.Date fechaHasta;
    private String observaciones;
    private Set<VueltaDTO> vueltasArr = new HashSet<>();

    public DiagramacionDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmpCodigo() {
        return empCodigo;
    }

    public void setEmpCodigo(String empCodigo) {
        this.empCodigo = empCodigo;
    }

    public Date getFecha_desde() {
        return getFechaDesde();
    }

    public void setFecha_desde(Date fecha_desde) {
        this.setFechaDesde(fecha_desde);
    }

    public Date getFecha_hasta() {
        return getFechaHasta();
    }

    public void setFecha_hasta(Date fecha_hasta) {
        this.setFechaHasta(fecha_hasta);
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public Set<VueltaDTO> getVueltasArr() {
        return vueltasArr;
    }

    public void setVueltasArr(Set<VueltaDTO> vueltasArr) {
        this.vueltasArr = vueltasArr;
    }

    /**
     * @return the fechaDesde
     */
    public java.util.Date getFechaDesde() {
        return fechaDesde;
    }

    /**
     * @param fechaDesde the fechaDesde to set
     */
    public void setFechaDesde(java.util.Date fechaDesde) {
        this.fechaDesde = fechaDesde;
    }

    /**
     * @return the fechaHasta
     */
    public java.util.Date getFechaHasta() {
        return fechaHasta;
    }

    /**
     * @param fechaHasta the fechaHasta to set
     */
    public void setFechaHasta(java.util.Date fechaHasta) {
        this.fechaHasta = fechaHasta;
    }

}
